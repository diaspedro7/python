raio = float(input())
n = 3.14159
area = n * (raio**2)

print(f"A={area:.4f}")

#EXPLICAÇÃO: O parâmetro f dentro do print permite customizar a funcao print
#Usando {} a gente coloca a variável que vai ser mostrada
#O :.4f formata o valor inserido
#O .4 signfica que terá 4 casas após o ponto decimal
#E o f significa que o valor eh um float, deixando um zero no final
